#include<bits/stdc++.h>
using namespace std;

int main() {
    int a = 8;
    float b = 3.14;
    char c = 'a';
    double d = 1234546;
    bool t = true;
    
    cout << "size of int is = " << sizeof(a) << endl;
    cout << "size of float is = " << sizeof(b) << endl;
    cout << "size of char is = " << sizeof(c) << endl;
    cout << "size of double is = " << sizeof(d) << endl;
    cout << "size of bool is = " << sizeof(t) << endl;
    
    return 0;
}
